def gen_pattern(chars):
    str_list = ['' for i in range(2*len(chars)-1)]
    for i in range(len(chars)):
        sub = chars[:-i-1:-1]+chars[-i-1:]
        sub = '.'.join(sub)
        str_list[i] = sub.center(4*len(chars)-3, '.') + '\n'
        str_list[-i-1] = sub.center(4*len(chars)-3, '.') + '\n'
    print(''.join(str_list))

